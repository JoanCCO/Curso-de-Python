#Definiendo una Variable
nombre = "Lucas"

#si se hace con la primera letra en mayuscula de la siguiente palabra se llama camelcase esto en Java
nombreCompleto = "Lucas"

#en Pyton se usa es guion bajo ejemplo
nombre_Completo = "Lucas"

#concatenar con +
bienvenida = "Hola " + nombre + " ¿Como estas?"
print (bienvenida)

#concatenar con  f-string
nombre = "F23"
bienvenida = f"Hola + {nombre} + ¿Como estas?"
print (bienvenida)

#operadores de pertenencia (in  / not in) nos dan True o False

print("Lucas" in bienvenida) #True
print ("Lucas" not in bienvenida) #False

